function prs = build_prs() % Todo: Add switch for other modes
    % ---------------------------------------------------------------------    
    % BUILD_PRS: Return phase reference symbol (currently for Mode 1 only)
    % ---------------------------------------------------------------------
    % Usage:
    %  Inputs
    %               n/a
    %
    %  Outputs
    %   > prs:      Phase Reference Symbol (for Mode 1)
    %
    % ---------------------------------------------------------------------
    % LUTs for Mode 1:
    k_table = [[-768, -737, 0, 1]
               [-736, -705, 1, 2]
               [-704, -673, 2, 0]
               [-672, -641, 3, 1]
               [-640, -609, 0, 3]
               [-608, -577, 1, 2]
               [-576, -545, 2, 2]
               [-544, -513, 3, 3]
               [-512, -481, 0, 2]
               [-480, -449, 1, 1]
               [-448, -417, 2, 2]
               [-416, -385, 3, 3]
               [-384, -353, 0, 1]
               [-352, -321, 1, 2]
               [-320, -289, 2, 3]
               [-288, -257, 3, 3]
               [-256, -225, 0, 2]
               [-224, -193, 1, 2]
               [-192, -161, 2, 2]
               [-160, -129, 3, 1]
               [-128,  -97, 0, 1]
               [-96,   -65, 1, 3]
               [-64,   -33, 2, 1]
               [-32,    -1, 3, 2]
               [  1,    32, 0, 3]
               [ 33,    64, 3, 1]
               [ 65,    96, 2, 1]
               [ 97,   128, 1, 1]
               [ 129,  160, 0, 2]
               [ 161,  192, 3, 2]
               [ 193,  224, 2, 1]
               [ 225,  256, 1, 0]
               [ 257,  288, 0, 2]
               [ 289,  320, 3, 2]
               [ 321,  352, 2, 3]
               [ 353,  384, 1, 3]
               [ 385,  416, 0, 0]
               [ 417,  448, 3, 2]
               [ 449,  480, 2, 1]
               [ 481,  512, 1, 3]
               [ 513,  544, 0, 3]
               [ 545,  576, 3, 3]
               [ 577,  608, 2, 3]
               [ 609,  640, 1, 0]
               [ 641,  672, 0, 3]
               [ 673,  704, 3, 0]
               [ 705,  736, 2, 1]
               [ 737,  768, 1, 1]];

    k_time_frequency_phase_table = ...
        [[0, 2, 0, 0, 0, 0, 1, 1, 2, 0, 0, 0, 2, 2, 1, 1, 0, 2, 0, 0, 0, 0, 1, 1, 2, 0, 0, 0, 2, 2, 1, 1 ]
        [0, 3, 2, 3, 0, 1, 3, 0, 2, 1, 2, 3, 2, 3, 3, 0, 0, 3, 2, 3, 0, 1, 3, 0, 2, 1, 2, 3, 2, 3, 3, 0 ]
        [0, 0, 0, 2, 0, 2, 1, 3, 2, 2, 0, 2, 2, 0, 1, 3, 0, 0, 0, 2, 0, 2, 1, 3, 2, 2, 0, 2, 2, 0, 1, 3 ]
        [0, 1, 2, 1, 0, 3, 3, 2, 2, 3, 2, 1, 2, 1, 3, 2, 0, 1, 2, 1, 0, 3, 3, 2, 2, 3, 2, 1, 2, 1, 3, 2 ]];

    % Build phase reference symbol
    prs = zeros(2048,1);
    idx_offset = 1025;

    % Calculated according to PRS generator algorithm
    % For more info, see DAB Standard document
    for r = k_table'
        for c = r(1):r(2)
            phi = pi/2 * (k_time_frequency_phase_table(r(3)+1,c-r(1)+1) + r(4));
            prs(c + idx_offset) = exp(1j*phi);
        end
    end
    
end